package com.ug.air.alrite.Models;

public class Cred2 {

    String code;
    String h_code;
    String counter;

    public Cred2(String code, String h_code, String counter) {
        this.code = code;
        this.h_code = h_code;
        this.counter = counter;
    }

    public String getCode() {
        return code;
    }

    public String getH_code() {
        return h_code;
    }

    public String getCounter() {
        return counter;
    }
}
